# Simplified Chinese — auto-convert from Traditional
def _build_zhcn():
    try:
        from opencc import OpenCC
        cc = OpenCC("t2s")
        zh_tw = TRANSLATIONS["zh-hant"]
        return {k: cc.convert(v) for k, v in zh_tw.items()}
    except Exception:
        return TRANSLATIONS["zh-hant"]

TRANSLATIONS["zh-hans"] = _build_zhcn()


def t(key: str, lang: str = "zh-hant", **kwargs) -> str:
    d = TRANSLATIONS.get(lang, TRANSLATIONS["zh-hant"])
    val = d.get(key, TRANSLATIONS["zh-hant"].get(key, key))
    if kwargs:
        try:
            val = val.format(**kwargs)
        except Exception:
            pass
    return val
